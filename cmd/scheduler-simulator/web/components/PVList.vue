<template>
  <v-row no-gutters  v-if="pvs.length !== 0 ">
    <v-col>
      <v-card class="ma-2" outlined>
        <v-card-title class="mb-1"> PersistentVolumes </v-card-title>
        <v-card-actions>
          <v-chip
            class="ma-2"
            v-for="(p, i) in pvs"
            :key="i"
            @click.stop="onClick(p)"
            color="primary"
            outlined
            large
            label
          >
            <img src="/pv.svg" height="40" alt="p.metadata.name" class="mr-2" />
            {{ p.metadata.name }}
          </v-chip>
        </v-card-actions>
      </v-card>
    </v-col>
  </v-row>
</template>

<script lang="ts">
import { V1PersistentVolume } from "@kubernetes/client-node";
import {
  ref,
  computed,
  inject,
  onMounted,
  PropType,
  defineComponent,
} from "@nuxtjs/composition-api";
import { getSimulatorIDFromPath } from "./lib/util";
import PersistentVolumeStoreKey from "./PVStoreKey";
export default defineComponent({
  setup(_, context) {
    const store = inject(PersistentVolumeStoreKey);
    if (!store) {
      throw new Error(`${PersistentVolumeStoreKey} is not provided`);
    }

    const getPVList = async () => {
      const route = context.root.$route;
      await store.list(getSimulatorIDFromPath(route.path));
    };
    const onClick = (pv: V1PersistentVolume) => {
      store.select(pv, false);
    };
    onMounted(getPVList);
    const pvs = computed(() => store.pvs);
    return {
      pvs,
      onClick,
    };
  },
});
</script>
